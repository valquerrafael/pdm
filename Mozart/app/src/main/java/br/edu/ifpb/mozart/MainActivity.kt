@Composable
fun PreviewMessageCard() {
    MozartTheme {
        Surface {
            MessageCard(
                msg = Message("Mozart", "Ei, olha essa composição, é incrível!")
            )
        }
    }
}

@Composable
fun Conversation(messages: List<Message>) {
    LazyColumn {
        items(messages) { message ->
            MessageCard(message)
        }
    }
}

